class Solution{
  public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        // code here
        // return the head of intersection list
        map<int,int> m;
        Node* temp=head2;
        while(temp)
        {
            m[temp->data]++;
            temp=temp->next;
        }
        temp=head1;
        Node* result= (Node*)malloc(sizeof(Node));
        Node* head=result;
        Node* prev=NULL;
        while(temp)
        {
            if(m[temp->data]>0)
            {
                prev=result;
                result->data=temp->data;
                m[temp->data]--;
                Node* temp1 = (Node*)malloc(sizeof(Node*));
                result->next=temp1;
                result=temp1;
            }
            temp=temp->next;
        }
        prev->next=NULL;
        return head;
    }
};

